"""CLI for masked-DINO frame scoring and strict paired aggregation."""

from __future__ import annotations

import argparse
import csv
import json
import os
import sys
from pathlib import Path
from typing import Any, Dict, List

import numpy as np

_SRC = Path(__file__).resolve().parents[3]
if str(_SRC) not in sys.path:
    sys.path.insert(0, str(_SRC))

from pipeline.v7.phase1.evaluation import (  # noqa: E402
    EvaluationError,
    read_per_frame_csv,
    validate_shared_mask,
    write_aggregate,
)
from pipeline.v7.phase1.manifest import load_manifest  # noqa: E402
from pipeline.v7.phase1.provenance import (  # noqa: E402
    validate_matched_run_invariants,
    validate_run_index_entry,
)
from pipeline.v7.phase1.raft import (  # noqa: E402
    attach_raft_scores,
    read_raft_scores,
    validate_raft_scores,
)


def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    sub = parser.add_subparsers(dest="command", required=True)
    aggregate = sub.add_parser("aggregate")
    aggregate.add_argument("--per_frame_csv", required=True)
    aggregate.add_argument("--output_json", required=True)
    aggregate.add_argument("--output_csv", required=True)
    aggregate.add_argument("--min_seeds", type=int, default=3)
    aggregate.add_argument("--bootstrap_seed", type=int, default=0)
    aggregate.add_argument("--manifest", action="append")
    aggregate.add_argument("--manifest_dir")
    aggregate.add_argument(
        "--seeds",
        help="optional assertion; set must exactly equal every manifest evaluation_seeds")
    aggregate.add_argument("--guardrails_json")

    score = sub.add_parser("score")
    score.add_argument("--manifest", required=True)
    score.add_argument("--gt_full", required=True)
    score.add_argument("--static_mask", required=True)
    score.add_argument("--mask_provenance", required=True)
    score.add_argument("--runs_index", required=True)
    score.add_argument(
        "--raft_scores_csv", required=True,
        help="strict per-frame RAFT evidence generated by raft_cli.py or equivalent")
    score.add_argument("--output_csv", required=True)
    score.add_argument("--device", default="cuda:0")
    return parser.parse_args()


def _score(args: argparse.Namespace) -> None:
    import torch
    import torch.nn.functional as F
    from pipeline.eval.oracle_injection import (
        _dino_feat, _read_video_back, _ssim, _to_gray_uint8,
    )

    manifest = load_manifest(args.manifest)
    if not os.path.isfile(args.gt_full):
        raise EvaluationError(f"GT_full missing: {args.gt_full}")
    gt = _read_video_back(args.gt_full)
    if gt is None or gt.shape[1] != manifest["total_frames"]:
        raise EvaluationError(
            f"GT_full must decode to {manifest['total_frames']} frames")
    mask = np.load(args.static_mask)
    with open(args.mask_provenance, encoding="utf-8") as handle:
        mask_provenance = json.load(handle)
    validate_shared_mask(mask, manifest["total_frames"], mask_provenance)
    with open(args.runs_index, encoding="utf-8") as handle:
        runs = json.load(handle)
    if not isinstance(runs, list) or not runs:
        raise EvaluationError("runs_index must contain a non-empty list")

    device = torch.device(args.device)
    records: List[Dict[str, Any]] = []
    events = {event["event_id"]: event for event in manifest["revisit_events"]}
    validated_runs = []
    for run in runs:
        if run["case_id"] != manifest["case_id"]:
            continue
        with open(run["provenance"], encoding="utf-8") as handle:
            provenance = json.load(handle)
        try:
            validate_run_index_entry(run, provenance, manifest=manifest)
        except ValueError as exc:
            raise EvaluationError(f"invalid run evidence: {exc}") from exc
        validated_runs.append(run)
    try:
        validate_matched_run_invariants(validated_runs, require_complete=True)
        raft_scores = validate_raft_scores(
            read_raft_scores(args.raft_scores_csv),
            runs=validated_runs,
            total_frames=manifest["total_frames"],
        )
    except ValueError as exc:
        raise EvaluationError(f"RAFT/matched evidence rejected: {exc}") from exc
    masked_target_cache = {}
    full_target_cache = {}
    for run in validated_runs:
        for key in ("case_id", "event_id", "seed", "arm", "video", "provenance"):
            if key not in run:
                raise EvaluationError(f"runs_index entry missing {key}")
        if run["case_id"] != manifest["case_id"] or run["event_id"] not in events:
            raise EvaluationError(f"runs_index entry does not match manifest: {run}")
        with open(run["provenance"], encoding="utf-8") as handle:
            provenance = json.load(handle)
        try:
            validate_run_index_entry(run, provenance, manifest=manifest)
        except ValueError as exc:
            raise EvaluationError(f"invalid run evidence: {exc}") from exc
        video = _read_video_back(run["video"])
        if (
            video is None
            or video.shape[1] != manifest["total_frames"]
            or video.shape[1] != run["actual_output_frames"]
        ):
            raise EvaluationError(f"generated video length mismatch: {run['video']}")
        event = events[run["event_id"]]
        for frame in range(manifest["total_frames"]):
            shared = mask[frame].astype(np.float32)[None]
            generated = video[:, frame] * shared
            target = gt[:, frame] * shared
            gf = _dino_feat(generated, device)
            if frame not in masked_target_cache:
                masked_target_cache[frame] = _dino_feat(target, device)
            tf = masked_target_cache[frame]
            if gf is None or tf is None:
                raise EvaluationError(f"DINO failed for {run['video']} frame={frame}")
            score = F.cosine_similarity(gf[None], tf[None], dim=-1)
            full_gf = _dino_feat(video[:, frame], device)
            if frame not in full_target_cache:
                full_target_cache[frame] = _dino_feat(gt[:, frame], device)
            full_tf = full_target_cache[frame]
            if full_gf is None or full_tf is None:
                raise EvaluationError(
                    f"full-frame DINO failed for {run['video']} frame={frame}")
            full_score = F.cosine_similarity(full_gf[None], full_tf[None], dim=-1)
            records.append({
                "case_id": run["case_id"],
                "event_id": run["event_id"],
                "seed": int(run["seed"]),
                "arm": run["arm"],
                "frame": frame,
                "region": (
                    "support"
                    if event["query_start"] <= frame < event["query_end"]
                    else "non_support"
                ),
                "masked_dino": float(score.item()),
                "full_dino": float(full_score.item()),
                "ssim": float(_ssim(
                    _to_gray_uint8(video[:, frame]),
                    _to_gray_uint8(gt[:, frame]),
                )),
                "mask_provenance": args.mask_provenance,
                "invariant_fingerprints": json.dumps(
                    run["invariant_fingerprints"], sort_keys=True),
            })
    try:
        attach_raft_scores(records, raft_scores)
    except ValueError as exc:
        raise EvaluationError(f"cannot attach RAFT evidence: {exc}") from exc
    output = Path(args.output_csv)
    output.parent.mkdir(parents=True, exist_ok=True)
    with output.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=list(records[0]))
        writer.writeheader()
        writer.writerows(records)


def main() -> None:
    args = _parse_args()
    if args.command == "score":
        _score(args)
    else:
        rows = read_per_frame_csv(args.per_frame_csv)
        manifest_paths = list(args.manifest or [])
        if args.manifest_dir:
            manifest_paths.extend(
                str(path) for path in sorted(Path(args.manifest_dir).glob("*.json"))
                if not path.name.endswith(".schema.json")
            )
        if not manifest_paths:
            raise EvaluationError("aggregate requires --manifest and/or --manifest_dir")
        manifests = {}
        for path in manifest_paths:
            manifest = load_manifest(path, require_review=True)
            case_id = manifest["case_id"]
            if case_id in manifests:
                raise EvaluationError(f"duplicate manifest case_id: {case_id}")
            manifests[case_id] = manifest
        guardrails = None
        if args.guardrails_json:
            with open(args.guardrails_json, encoding="utf-8") as handle:
                guardrails = json.load(handle)
        write_aggregate(
            rows,
            args.output_json,
            output_csv=args.output_csv,
            min_seeds=args.min_seeds,
            bootstrap_seed=args.bootstrap_seed,
            manifests=manifests,
            seeds=(
                [int(value) for value in args.seeds.split(",")]
                if args.seeds else None
            ),
            guardrails=guardrails,
        )


if __name__ == "__main__":
    main()
